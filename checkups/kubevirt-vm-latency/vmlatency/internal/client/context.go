package client

import "context"

func withContext(ctx context.Context, f func()) error {
	done := make(chan struct{}, 1)

	go func() {
		f()
		done <- struct{}{}
	}()

	select {
	case <-done:
		return nil
	case <-ctx.Done():
		return ctx.Err()
	}
}
